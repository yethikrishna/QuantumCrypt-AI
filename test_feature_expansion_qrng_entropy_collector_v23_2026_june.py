class TestEntropySample(unittest.TestCase):
    """Tests for EntropySample dataclass."""

    def test_sample_creation(self):
        sample = EntropySample(b'\x00\x01\x02\x03', EntropySourceType.SYSTEM)
        self.assertEqual(len(sample.data), 4)
        self.assertEqual(sample.source_type, EntropySourceType.SYSTEM)
        self.assertIsNotNone(sample.sample_id)

    def test_entropy_estimation(self):
        # High entropy data
        high_entropy = bytes(range(256))
        sample = EntropySample(high_entropy, EntropySourceType.SYSTEM)
        self.assertGreater(sample.estimated_entropy, 0)

    def test_empty_sample(self):
        sample = EntropySample(b'', EntropySourceType.SYSTEM)
        self.assertEqual(sample.estimated_entropy, 0.0)


